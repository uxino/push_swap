#include "libft.h"

void	ft_atoi_check(const char *s)
{
	int	i;

	i = 0;
	if ((s[i] != '-') && (!(s[i] <= '9' && s[i] >= '0')))
		ft_error(31);
	if (s[i] == '-')
		i++;
	write(1, "a\n", 2);
	if (!(s[i] <= '9' && s[i] >= '0'))
		ft_error(31);
	while (s[i] <= '9' && s[i] >= '0')
		i++;
}
